package com.qfedu.a_test;

public class Demo2 {
	public static void main(String[] args) {
		//数据类型  变量的名字 = 初始化的值;
		int a = 10;//将10赋值给变量a了
		//拿a去进行操作
		System.out.println(a);//10
		a = 20;//将20赋值给了 a
		System.out.println(a);//20
		//其他的声明方式
		int b;//将变量的名字先定义出来。，然后再赋值
		b = 20;
		int c,d;//一次性定义两个变量
		
	}

}
