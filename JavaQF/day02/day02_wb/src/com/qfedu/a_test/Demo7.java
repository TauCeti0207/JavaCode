package com.qfedu.a_test;

public class Demo7 {
	public static void main(String[] args) {
		//打印一个字符类型的数据   '
		//\'将'转为一个无意义的字符
		System.out.println('\'');
		//打印一个字符类型的\
		System.out.println('\\');
		//打印一个字符串 "
		System.out.println("\"");
		//打印一个字符串 \
		System.out.println("\\");
		//打印一个字符串 \\
		System.out.println("\\\\");
		//\n
		System.out.println("巴萨不仅仅\n是今年是解决");
		System.out.println("是南京南京\t叫撒解决");
		
	}

}
